division=(numero1 / numero2)

print(f'La suma de los numeros {numero1} y {numero2} es {suma}')
print(f'La suma de los numeros {numero1} y {numero2} es {resta}')
print(f'La suma de los numeros {numero1} y {numero2} es {multi}')
print(f'La suma de los numeros {numero1} y {numero2} es {division}')